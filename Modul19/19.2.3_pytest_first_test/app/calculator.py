

class Calculator:
   def multiply(self, x, y):
       """Функция умножения"""
       return x * y

   def division(self, x, y):
       """Функция деления"""
       return x / y

   def subtraction(self, x, y):
       """Функция вычитания"""
       return x - y

   def adding(self, x, y):
       """Функция сложения"""
       return x + y